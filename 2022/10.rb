row = Array.new(40)
cycles = 0
image = []
res = []
x = 1

input.each { |op, arg|
  op_cycles = op == "addx" ? 2 : 1

  (op_cycles).times {
    row[cycles % 40] = (cycles % 40).between?(x - 1, x + 1) ? "#" : "."

    cycles += 1
    image << row.map(&:clone) if (cycles % 40) == 0

    res << (cycles * x) if cycles % 40 == 20
  }

  x += arg.to_i if op == "addx"
}

p res.sum

image.each { |row| p row.join }

